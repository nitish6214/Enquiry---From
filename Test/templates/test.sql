create table employee
( 
   employee_id  int,
   name varchar(30),
   salary varchar(20),
   nsalary varchar(20)
  );


create table enquiry
( 
   s_no  int,
   name varchar(30),
   dob varchar(20),
   gender varchar(10),
   father_name varchar(15),
   address varchar(30),
   contact_no  varchar(20),
   email_id varchar(20),
   education_qualification varchar(40),
   school_college varchar(30),
   course_interested varchar(30),
   date  varchar(30),
   counseled varchar(50),
   signature  varchar(40)
  );
